     <h3>What is Disease?</h3>
    <br><strong> “A disease is a condition that deteriorates the normal functioning of the cells, tissues, and organs.”</strong>
    Diseases are often thought of as medical conditions that are characterized by their signs and symptoms.
<br>
    The disease can also be defined as:
    <br>
    <strong> “Any dangerous divergence from a functional or normal state of an entity.”</strong>
</div>
</div>
<br><br>
<div class="row">
    <div class="col col-12 col-xs-12 col-sm-12 col-md-12 col-lg-12 col-xl-12 col-xxl-12">
        When a person is inflicted with a disease, he exhibits a few symptoms and signs that range from normal to severe depending upon the medical condition. Hence, in order to identify different diseases, the normalcy of an entity needs to be studied and understood as a clear demarcation between disease and disease-free is not always apparent.
<br>
The diseases are usually caused by many factors rather than a single cause. When we have a disease, we eventually show some signs, such as headaches, cough, cold, weakness. These signs are referred to as <strong>“symptoms.”</strong> In almost all diseases, symptoms are shown immediately after having been struck by the disease. However, it varies depending upon the seriousness of the disease.
